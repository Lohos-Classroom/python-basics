## une variable se defini en commençant par son nom, puis egal, et enfin sa valeur 
variable = 1

## il existe 4 types de variables basiques (Integer, Float, String, boolean)

## Les Nombres entiers : Integer ou Int en abregé
Integer = 1

## Les nombres a virgule : Float (avec un point a la place de la virgule)
Float = 2.14

## Les Chaines de caracteres : String (des mots, lettre ou phrases, il s'ecrivent entre guillemets)
String = "Bientot Fini ?"

## Les Vrai ou Faux : Boolean (ecrit en anglais True et False)
Boolean1 = True
Boolean2 = False



## Essayons d'afficher ces variables : 
print(f"Integer :  {Integer}")
print(f"Float : {Float}")
print(f"String : {String}")
print(f"Boolean : {Boolean1} {Boolean2}")